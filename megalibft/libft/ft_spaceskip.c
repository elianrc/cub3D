#include "libft.h"

int		ft_spaceskip(char *line)
{
	int i;

	i = 0;
	while ((*line == ' ' || *line == '\t' || *line == '\n')
	|| (*line == '\r' || *line == '\v' || *line == '\f'))
	{
		line++;
		i++;
	}
	return (i);
}